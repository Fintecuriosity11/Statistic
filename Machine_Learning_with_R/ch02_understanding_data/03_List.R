###리스트(List)

# List는 항목의 순서 집합을 저장하는 데 사용된다는 점에서 벡터와 매우 유사한 데이터 구조임.
# 하지만 벡터(vector)는 모든 항목이 같은 타입이어야 한다는 단점이 있음.
# 반면에 리스트는 다양한 타입의 입출력 데이터와 머신러닝 모델의 설정 파라미터 집합을 저장하는 데 자주 사용됨.

# 6개의 벡터에 저장된 3명의 환자 데이터로 구축된 의료 환자 데이터셋을 고려.
# 첫번째 환자의 데이터를 보여주려면 아래와 같이 5개 명령 입력.

subject_name[1]

temperature[1]

flu_status[1]

gender[1]

blood[1]

symptoms[1]


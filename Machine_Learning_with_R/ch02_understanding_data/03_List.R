####################################################################################################################################

# 미래에 환자의 데이터를 다시 조사한다고 하면 위의 명령을 다시 입력하는 대신 List를 사용하면 모든 값을 하나의 객체로 그룹화
# 추후 반복적으로 사용 가능.

# c()함수로 벡터를 생성하는 것처럼 list()함수로 리스트를 생성할 수 있음.
# 차이점이라고 하면 리스트를 구성할 때 열의 각 구성요소에는 이름이 주어져야 함.

# 이름이 반드시 필요하지는 않지만, 이를 통하여 나중에 리스트 값에 접근할 때 숫자로 된 위치가 아닌 이름으로 접근할 수 있음.
# 첫 환자의 데이터 전체에 대해 이름이 있는 구성 요소로 이뤄진 리스트를 생성하려면 다음과 같이 입력.

subject1 <- list(fullname = subject_name[1],
                 temperature= temperature[1],
                 flu_status= flu_status[1],
                 gender= gender[1],
                 blood= blood[1],
                 symptoms= symptoms[1])


########################################################결과값(print)###############################################################

# > subject1 <- list(fullname = subject_name[1],
#                   +                  temperature= temperature[1],
#                   +                  flu_status= flu_status[1],
#                   +                  gender= gender[1],
#                   +                  blood= blood[1],
#                   +                  symptoms= symptoms[1])


####################################################################################################################################

subject1$fullname

subject1$temperature

subject1$flu_status

subject1$gender

subject1$blood

subject1$symptoms


########################################################결과값(print)###############################################################

# > subject1$fullname
# [1] "John Doe"

# > subject1$temperature
# [1] 98.1

# > subject1$flu_status
# [1] FALSE


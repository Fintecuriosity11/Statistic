####################################################################################################################################

subject1[2]$temperature


# 환자의 데이터는 subject1 리스트에 수집.
# 이전 명령에서 지정된 이름으로 값이 레이블된 점 확인.
# 리스트는 벡터처럼 순서를 지키므로 구성 요소도 temperature 값에서 본 것처럼 수치로 된 위치 값으로 접근.

########################################################결과값(print)###############################################################

# > subject1[2]$temperature
# [1] 98.1

####################################################################################################################################

subject1[[2]]

# 리스트에 벡터 스타일 연산자를 적용한 결과는 새로운 리스트 객체로, 원래 리스트의 부분집합임.
# 예를 들어 앞으 코드는 단일 temperature 구성 요소를 갖는 리스트를 반환함.
# 기본 데이터 타입을 갖는 하나의 리스트 항목을 반환하려면 리스트의 구성 요소를 선택할 때 이중 괄호 [[ ]]를 사용.
# 아래의 결과값은 다음의 길이ㅏ 1인 수치 벡터를 반환함.

########################################################결과값(print)###############################################################

# > subject1[[2]]
# [1] 98.1

####################################################################################################################################

subject1$temperature

# 명확하게 하고자 리스트 구성 요소에 접근할 때 $와 리스트 구성 요소 이름을 다음에 보는 것처럼 리스트 이름에 붙여 접근.

# 리스트 값에 이름으로 접근하면 나중에 리스트 항목의 순서가 바뀌어도 정확한 항목이 검색 가능.

########################################################결과값(print)###############################################################

# > subject1$temperature
# [1] 98.1

####################################################################################################################################

subject1[c("temperature", "flu_status")]$temperature

subject1[c("temperature", "flu_status")]$flu_status

# 이름 벡터를 명시해 리스트의 여러 항목을 얻을 수도 있음.
# temperature와 flu_status 구성 요소로 된 subject1 리스트의 부분집합을 반환함.

# 전체 데이터셋을 리스트와 리스트의 리스트를 사용해 구출할 수도 있음.
# 예를 들자면 subject2와 subject3 리스트를 생성한 이것들을 pt_data란 이름의 한 리스트 객체로 묶는 것도 고려할 수 있음.

########################################################결과값(print)###############################################################

